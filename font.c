static int utf8_next_codepoint(const unsigned char **ptr)
{
	const unsigned char *p = *ptr;
	int c = *p++;

	if (c < 0x80) {
		*ptr = p;
		return c;
	}

	if ((c & 0xE0) == 0xC0 && p[0] && (p[0] & 0xC0) == 0x80) {
		int codepoint = ((c & 0x1F) << 6) | (p[0] & 0x3F);
		*ptr = p + 1;
		return codepoint;
	}

	if ((c & 0xF0) == 0xE0 &&
	    p[0] && p[1] &&
	    (p[0] & 0xC0) == 0x80 &&
	    (p[1] & 0xC0) == 0x80) {
		int codepoint = ((c & 0x0F) << 12) |
		                ((p[0] & 0x3F) << 6) |
		                (p[1] & 0x3F);
		*ptr = p + 2;
		return codepoint;
	}

	if ((c & 0xF8) == 0xF0 &&
	    p[0] && p[1] && p[2] &&
	    (p[0] & 0xC0) == 0x80 &&
	    (p[1] & 0xC0) == 0x80 &&
	    (p[2] & 0xC0) == 0x80) {
		int codepoint = ((c & 0x07) << 18) |
		                ((p[0] & 0x3F) << 12) |
		                ((p[1] & 0x3F) << 6) |
		                (p[2] & 0x3F);
		*ptr = p + 3;
		return codepoint;
	}

	*ptr = p;
	return '?';
}

static bool atlas_supports_codepoint(int codepoint)
{
	return (codepoint >= 32 && codepoint <= 126) ||
	       (codepoint >= 160 && codepoint <= 255);
}

static bool font_needs_unicode_fallback(const char *text)
{
	const unsigned char *p = (const unsigned char *)text;

	while (*p) {
		int codepoint = utf8_next_codepoint(&p);
		if (!atlas_supports_codepoint(codepoint))
			return true;
	}

	return false;
}

static const unsigned char *font_glyph_rows(int idx)
{
	if (idx < 0 || idx >= FONT_NUM_CHARS)
		return NULL;

	if (idx < 95)
		return font_data[idx];

	return font_data_ext[idx - 95];
}

