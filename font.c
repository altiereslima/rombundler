static uint32_t *font_sw_buf = NULL;
static int font_sw_w = 0, font_sw_h = 0;

void font_set_sw_target(uint32_t *buf, int w, int h)
{
	font_sw_buf = buf;
	font_sw_w = w;
	font_sw_h = h;
}

void font_clear_sw_target(void)
{
	font_sw_buf = NULL;
}

static inline void sw_put_pixel(int x, int y, uint32_t color)
{
	if (x >= 0 && x < font_sw_w && y >= 0 && y < font_sw_h)
		font_sw_buf[y * font_sw_w + x] = color;
}

static inline void sw_blend_pixel(int x, int y, font_color color, unsigned char coverage)
{
	uint32_t dst;
	unsigned int src_a;
	unsigned int inv_a;
	unsigned int src_r;
	unsigned int src_g;
	unsigned int src_b;
	unsigned int dst_r;
	unsigned int dst_g;
	unsigned int dst_b;
	unsigned int out_r;
	unsigned int out_g;
	unsigned int out_b;
	unsigned int out_a;

	if (x < 0 || x >= font_sw_w || y < 0 || y >= font_sw_h || coverage == 0)
		return;

	dst = font_sw_buf[y * font_sw_w + x];
	src_a = (unsigned int)(color.a * (float)coverage);
	inv_a = 255u - src_a;
	src_r = (unsigned int)(color.r * 255.0f);
	src_g = (unsigned int)(color.g * 255.0f);
	src_b = (unsigned int)(color.b * 255.0f);
	dst_r = (dst >> 16) & 0xFFu;
	dst_g = (dst >> 8) & 0xFFu;
	dst_b = dst & 0xFFu;
	out_r = (src_r * src_a + dst_r * inv_a) / 255u;
	out_g = (src_g * src_a + dst_g * inv_a) / 255u;
	out_b = (src_b * src_a + dst_b * inv_a) / 255u;
	out_a = src_a + (((dst >> 24) & 0xFFu) * inv_a) / 255u;

	font_sw_buf[y * font_sw_w + x] =
		(out_b & 0xFFu) |
		((out_g & 0xFFu) << 8) |
		((out_r & 0xFFu) << 16) |
		((out_a & 0xFFu) << 24);
}

static void font_render_text_sw(float x, float y, const char *text,
                                font_color color, float scale)
{
	if (!text || !text[0]) return;

	uint32_t pixel = ((uint32_t)(color.b * 255.0f))       |
	                 ((uint32_t)(color.g * 255.0f) << 8)   |
	                 ((uint32_t)(color.r * 255.0f) << 16)  |
	                 ((uint32_t)(color.a * 255.0f) << 24);

	float cx = x;
	float cw = FONT_CHAR_W * scale;
	float ch = FONT_CHAR_H * scale;

	for (const unsigned char *p = (const unsigned char *)text; *p;) {
		int codepoint = utf8_next_codepoint(&p);
		int idx = -1;
		const unsigned char *glyph_rows;

		if (codepoint >= 32 && codepoint <= 126)
			idx = codepoint - 32;
		else if (codepoint >= 160 && codepoint <= 255)
			idx = 95 + (codepoint - 160);

		glyph_rows = font_glyph_rows(idx);
		if (glyph_rows) {
			int icw = (int)(cw + 0.5f);
			int ich = (int)(ch + 0.5f);
			for (int row = 0; row < ich; row++) {
				int src_row = row * FONT_CHAR_H / ich;
				if (src_row >= FONT_CHAR_H) src_row = FONT_CHAR_H - 1;
				unsigned char bits = glyph_rows[src_row];
				for (int col = 0; col < icw; col++) {
					int src_col = col * FONT_CHAR_W / icw;
					if (bits & (0x80 >> src_col))
						sw_put_pixel((int)cx + col, (int)y + row, pixel);
				}
			}
		}
		cx += cw;
	}
}

